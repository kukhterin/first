#include "Socket.hpp"
#include <iostream>
#include <fcntl.h>
#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <netdb.h>
#include <string.h>
#include <errno.h>

Socket::Socket(const int flags, const char* port, const int connections)
{
	create_and_bind(flags, port);
	make_non_blocking();
	Listen(connections);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Socket::~Socket()
{
	close(fd_);
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Socket::Socket(const Socket& socket)
{
	if(fd_ != socket.fd_)
		fd_ = socket.fd_;
	else
		std::cout << "Same socket!" << std::endl;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Socket::operator=(const Socket& socket)
{
	if(fd_ != socket.fd_)
		fd_ = socket.fd_;
	else
		std::cout << "Same socket!" << std::endl;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Socket::Listen(const int connections) const
{
	if (listen(fd_, connections) == -1 )
	{
		std::cout << "Listen error: " << strerror(errno) << std::endl;;
		exit(-1);
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Socket::Accept()
{
	addrlen_ = sizeof sockaddr_;
	int new_fd = accept(fd_, (struct sockaddr *) &sockaddr_, &addrlen_);
	if (new_fd == -1)
	{
		if ((errno != EAGAIN) && (errno != EWOULDBLOCK))
			s_err("Accept error");
	}

	return new_fd;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int Socket::get_fd()
{
	return fd_;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Socket::create_and_bind(const int flags, const char* port)
{
	struct addrinfo hints, *res, *rp;
	// getaddrinfo for host
	memset (&hints, 0, sizeof(hints));
	hints.ai_family = AF_INET;
	hints.ai_socktype = SOCK_STREAM;
	hints.ai_flags = flags;						//for wildcard IP address 
	if (getaddrinfo(NULL, port, &hints, &res) != 0)
	{
		s_err("getaddrinfo() error");
		exit(-1);
	}

	for (rp = res; rp != NULL; rp = rp->ai_next)
	{
		fd_ = socket (rp->ai_family, rp->ai_socktype, 0);
		if (fd_ == -1) 
			continue;
		
		if (bind(fd_, rp->ai_addr, rp->ai_addrlen) == 0) 
			break; //success
	}
	if (rp == NULL) 					//no address succeeded 
	{
		s_err("Could not bind");
		exit(-1);
	}
	freeaddrinfo(res); 					//no longer needed
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Socket::make_non_blocking()
{
	int flags, s;
	
	flags = fcntl (fd_, F_GETFL, 0);
	if (flags == -1)
		{
		s_err("fcntl error");
		exit(-1);
		}

	flags |= O_NONBLOCK;
	s = fcntl (fd_, F_SETFL, flags);
	if (s == -1)
		{
			s_err("Fcntl");
			exit(-1);
		}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Socket::s_err(std::string s)
{
	std::cout << s << ": " << strerror(errno) << std::endl;
}
